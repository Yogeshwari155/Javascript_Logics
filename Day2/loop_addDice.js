//Increment type
var sum=0
for(var i=1;i<=5;i++)
{
a=Math.floor(Math.random()*90+10)
sum+=a
}
var avg = sum/5
console.log("Increment type")
console.log("Average of 5 random two digit numbers is : "+avg)

//Decrement type
var sum=0
for(var i=5;i>=1;i--)
{
a=Math.floor(Math.random()*90+10)
sum+=a
}
var avg = sum/5
console.log("Decrement type")
console.log("Average of 5 random two digit numbers is : "+avg)